// Operadores de Atribuição

int a = 10;
Console.WriteLine(a); 

a += 5;
Console.WriteLine(a); // a = a + 5;

a -= 3;
Console.WriteLine(a); // a = a - 3;

a *= 2;
Console.WriteLine(a); // a = a * 2;


